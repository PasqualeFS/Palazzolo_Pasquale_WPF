/* It's called "Ternary" because this operator requires three components.  It uses a ? */

/* A standard conditional/if statement looks like this:


if(condition){
    do if true;
} else {
    do if false;
}

 */

/* A Ternary operator would look like this:

(condition) ? do if true : do if false;

 */



